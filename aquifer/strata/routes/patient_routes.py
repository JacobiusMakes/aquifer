"""Patient portability routes — identity, consent, and cross-practice transfer."""

from __future__ import annotations

import re

from fastapi import APIRouter, HTTPException, Request
from pydantic import BaseModel

from aquifer.core import PRACTICE_TYPE_DEFAULTS
from aquifer.strata.auth import AuthContext

router = APIRouter(prefix="/patients", tags=["patients"])


# ---------------------------------------------------------------------------
# Request / Response models
# ---------------------------------------------------------------------------

class RegisterPatientRequest(BaseModel):
    email: str
    phone: str | None = None

    @classmethod
    def __get_validators__(cls):
        yield cls._validate

    @classmethod
    def _validate(cls, v):
        return v


class RegisterPatientResponse(BaseModel):
    patient_id: str
    email: str
    share_key: str | None = None
    message: str = "Patient registered. Verify email to enable transfers."


class VerifyPatientRequest(BaseModel):
    patient_id: str
    otp: str


class VerifyPatientResponse(BaseModel):
    verified: bool
    message: str
    share_key: str | None = None


class CreateConsentRequest(BaseModel):
    source_practice_id: str
    target_practice_id: str
    scope: str = "all"  # comma-separated domains, or "all"
    practice_type: str | None = None  # if set, auto-fills scope from PRACTICE_TYPE_DEFAULTS


class ConsentResponse(BaseModel):
    consent_id: str
    patient_id: str
    source_practice_id: str
    target_practice_id: str
    scope: str
    status: str
    authorized_at: str | None
    expires_at: str
    created_at: str


class TransferRequest(BaseModel):
    consent_id: str


class TransferResponse(BaseModel):
    transfer_id: str
    consent_id: str
    token_count: int
    status: str
    created_at: str


class PracticeLink(BaseModel):
    practice_id: str
    source_file_hashes: str | None
    linked_at: str


class PullRequest(BaseModel):
    share_key: str
    practice_type: str | None = None


class PullTransferSummary(BaseModel):
    transfer_id: str
    source_practice_id: str
    token_count: int
    status: str


class PullResponse(BaseModel):
    patient_email_masked: str
    transfers: list[PullTransferSummary]
    total_tokens: int


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

_EMAIL_RE = re.compile(r"^[^@\s]+@[^@\s]+\.[^@\s]+$")


def _validate_email(email: str) -> str:
    email = email.lower().strip()
    if not _EMAIL_RE.match(email):
        raise HTTPException(422, "Invalid email address")
    return email


def _require_scope(auth: AuthContext, scope: str) -> None:
    """Ensure the auth context has the required API key scope (JWT always passes)."""
    from aquifer.strata.auth import has_api_key_scopes
    if not has_api_key_scopes(auth, scope):
        raise HTTPException(403, f"API key missing required '{scope}' scope")


